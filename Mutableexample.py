# Mutable - list example
list_example = [1, 2, 3]
list_example[0] = 4 # This is valid
print(list_example) # Output: [4, 2, 3]

# Mutable: Mutable objects, on the other hand, can be modified after they are created. This means that their state can change over time without creating a new object. Mutable objects allow in-place modifications, meaning you can change their content directly.
# Examples of mutable objects in Python include lists, dictionaries, sets, and custom objects. For instance, if you have a list in Python, you can modify its elements, add new elements, or remove existing elements without creating a new list object each time.