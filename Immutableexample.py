# Immutable: Objects that are immutable cannot be modified after they are created. Any operation that appears to modify the object actually creates a new object with the modified value. Immutable objects guarantee that their state remains constant throughout their lifetime.

# Examples of immutable objects in Python include tuples, strings, integers, and frozensets. For instance, if you try to modify a string in Python, you're essentially creating a new string object with the modified content rather than modifying the original string object.